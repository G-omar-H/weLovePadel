// Get translation for email
export function getEmailT(lang, type, key, ...args) {
    const translations = emailTranslations[lang] || emailTranslations.en;
    const typeTranslations = translations[type] || translations.customer;
    const translation = typeTranslations[key];

    if (typeof translation === 'function') {
        return translation(...args);
    }
    return translation || emailTranslations.en[type][key] || key;
}

export default emailTranslations;
